function setRailTitle(titleId, text) {
  const heading = document.getElementById(titleId);
  if (!heading) return;
  heading.innerHTML = `${text}<span class="rail__title-arrow" aria-hidden="true">&#8594;</span>`;
}

function applyExploreMode(mode) {
  const safeMode = modeRails[mode] ? mode : "homes";

  if (typeof window.initRails === "function") {
    window.initRails(modeRails[safeMode]);
  }

  const titleMap = modeTitles[safeMode];
  Object.entries(titleMap).forEach(([titleId, text]) => {
    setRailTitle(titleId, text);
  });

  const inspirationSection = document.querySelector(".inspiration");
  if (inspirationSection) {
    inspirationSection.hidden = safeMode !== "homes";
  }

  if (typeof window.setSearchMode === "function") {
    window.setSearchMode(safeMode);
  }

  document.body.setAttribute("data-explore-mode", safeMode);
}

function initInspirationTabs() {
  const tabList = document.querySelector(".inspiration__tabs");
  if (!tabList) return;

  const tabs = Array.from(tabList.querySelectorAll(".inspiration__tab"));
  const panels = Array.from(document.querySelectorAll(".inspiration__panel"));
  if (!tabs.length || !panels.length) return;

  const setActiveTab = (nextTab, shouldFocus = false) => {
    if (!nextTab) return;
    const nextPanelId = nextTab.dataset.inspirationTab;

    tabs.forEach((tab) => {
      const isActive = tab === nextTab;
      tab.classList.toggle("inspiration__tab--active", isActive);
      tab.setAttribute("aria-selected", String(isActive));
      tab.setAttribute("tabindex", isActive ? "0" : "-1");
    });

    panels.forEach((panel) => {
      const isActive = panel.id === nextPanelId;
      panel.classList.toggle("inspiration__panel--active", isActive);
      panel.hidden = !isActive;
    });

    if (shouldFocus) {
      nextTab.focus();
    }
  };

  tabs.forEach((tab, index) => {
    tab.addEventListener("click", () => {
      setActiveTab(tab);
    });

    tab.addEventListener("keydown", (event) => {
      let targetIndex = index;

      if (event.key === "ArrowRight") {
        targetIndex = (index + 1) % tabs.length;
      } else if (event.key === "ArrowLeft") {
        targetIndex = (index - 1 + tabs.length) % tabs.length;
      } else if (event.key === "Home") {
        targetIndex = 0;
      } else if (event.key === "End") {
        targetIndex = tabs.length - 1;
      } else {
        return;
      }

      event.preventDefault();
      setActiveTab(tabs[targetIndex], true);
    });
  });

  const initiallyActive =
    tabs.find((tab) => tab.classList.contains("inspiration__tab--active")) || tabs[0];
  setActiveTab(initiallyActive);
}

document.addEventListener("DOMContentLoaded", () => {
  if (typeof window.initSearch === "function") {
    window.initSearch();
  }

  initInspirationTabs();

  window.setExploreMode = (mode) => {
    applyExploreMode(mode);
  };

  const activeTab = document.querySelector(".hero-tab.hero-tab--active");
  window.setExploreMode(activeTab?.dataset.tab || "homes");
});
